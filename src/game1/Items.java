package game1;

import java.awt.Rectangle;

public class Items {
	protected int x;
	protected int y;
	protected int height;
	protected int width;
	
	
	public Items(int x, int y, int height, int width) {
		super();
		this.x = x;
		this.y = y;
		this.height = height;
		this.width = width;
	}
	public int getX() {
		return x;
	}
	public int getY() {
		return y;
	}
	public int getHeight() {
		return height;
	}
	public int getWidth() {
		return width;
	}
	public Rectangle getItems() {
		return new Rectangle(x, y, width, height);
	}
	public void setX(int x) {
		this.x = x;
	}
	public void setY(int y) {
		this.y = y;
	}
}
